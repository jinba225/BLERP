/**
 * 获取图标 SVG 代码
 * @param {string} iconName - 图标名称
 * @param {string} sizeClass - 尺寸类名，默认 'icon'
 * @returns {string} SVG 代码
 */
function getIcon(iconName, sizeClass = 'icon') {
  const icon = HEROICONS[iconName];
  if (!icon) {
    console.warn(`图标 "${iconName}" 不存在，请检查 heroicons-icons.js`);
    return '';
  }
  return icon.svg.replace('class="icon"', `class="${sizeClass}"`);
}

/**
 * 获取所有图标名称列表
 * @returns {Array<string>} 图标名称数组
 */
function getAllIconNames() {
  return Object.keys(HEROICONS);
}

/**
 * 按分类获取图标
 * @returns {Object} 分类后的图标对象
 */
function getIconsByCategory() {
  const categories = {};
  Object.entries(HEROICONS).forEach(([name, icon]) => {
    const category = name.split('-')[0];
    if (!categories[category]) {
      categories[category] = {};
    }
    categories[category][name] = icon;
  });
  return categories;
}

