            class_match = re.search(r'class="([^"]*)"', match.group(0))
            additional_classes = ''
            if class_match:
                classes = class_match.group(1).split()
                # ËøáÊª§ÊéâÂ∞∫ÂØ∏Á±ªÔºàÊàë‰ª¨‰ºöÁî®Áªü‰∏ÄÁöÑ .icon Á±ªÔºâ
